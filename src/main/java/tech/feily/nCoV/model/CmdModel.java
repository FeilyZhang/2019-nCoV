package tech.feily.nCoV.model;

public class CmdModel {

    private String cmd;
    private String opt;
    
    public void setCmd(String cmd) {
        this.cmd = cmd;
    }
    public void setOpt(String opt) {
        this.opt = opt;
    }
    
    public String getCmd() {
        return cmd;
    }
    public String getOpt() {
        return opt;
    }
    
}
